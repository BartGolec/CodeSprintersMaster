package calculator;

public class CalculatorApp {

    public int add(int a, int b){
        return a+b;
    }

    public double add(double a, double b) {
        return a + b;
    }

    public int division(int divident, int divider){
        return divident/divider;
    }

    public double division(double divident, double divider){
        return divident/divider;
    }
}
